# Creating variables (these are like labeled boxes!)
our_integer = 10 #whole number
our_float = 20.5 #float for decimal numbers
our_string = "Hello, Python!" #string for text
our_bool = True #boolean for true/false
print(our_integer, our_float, our_string, our_bool)

# Let's see what type each variable is!
print(type(our_integer), type(our_float), type(our_string), type(our_bool))